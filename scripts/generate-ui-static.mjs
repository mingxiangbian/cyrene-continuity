#!/usr/bin/env node
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const staticRoot = resolve(repoRoot, 'src/ui/static')
const outfile = resolve(repoRoot, 'src/codex/codex-ui-static.generated.ts')

const assets = {
  '/': {
    sourcePath: resolve(staticRoot, 'index.html'),
    contentType: 'text/html; charset=utf-8'
  },
  '/app.js': {
    sourcePath: resolve(staticRoot, 'app.js'),
    contentType: 'text/javascript; charset=utf-8'
  },
  '/styles.css': {
    sourcePath: resolve(staticRoot, 'styles.css'),
    contentType: 'text/css; charset=utf-8'
  }
}

const generatedAssets = Object.fromEntries(
  await Promise.all(
    Object.entries(assets).map(async ([pathname, asset]) => [
      pathname,
      {
        contentType: asset.contentType,
        body: await readFile(asset.sourcePath, 'utf8')
      }
    ])
  )
)

const source = `// Generated by scripts/generate-ui-static.mjs. Do not edit manually.

export interface CodexUiStaticAsset {
  contentType: string
  body: string
}

export const CODEX_UI_STATIC_ASSETS: Record<string, CodexUiStaticAsset> = ${JSON.stringify(generatedAssets, null, 2)}
`

await mkdir(dirname(outfile), { recursive: true })
await writeFile(outfile, source)
